// Story series — narrative adventures for immersive language learning.
// NPC dialogue is generated at runtime by Claude; these are intent/structure definitions.

export type StepType = "scene" | "say" | "choice" | "quiz" | "ar-circle";

export interface StoryStep {
  id: string;
  type: StepType;
  sceneContext: string;        // English: what's happening (used for AI prompt)
  npcName: string;             // who's talking
  npcIntentEn: string;         // what the NPC intends to say (English, translated at runtime)
  userTaskEn: string;          // what the user must do (shown as instruction)
  vocabEn: string[];           // 3–5 key words this step teaches
  targetPhraseEn?: string;     // for "say" steps: phrase user should produce
  choices?: { textEn: string; correct: boolean }[];  // for "choice" steps
  arItem?: string;             // for "ar-circle": item to find/circle in camera
  level: number;               // 1=beginner (single words) → 5=advanced (full convo)
  sceneEmoji: string;
  bgColor: string;             // Tailwind gradient classes
}

export interface StoryChapter {
  id: string;
  chapterNum: number;
  title: string;
  steps: StoryStep[];
}

export interface StorySeries {
  id: string;
  title: string;
  emoji: string;
  tagline: string;
  chapters: StoryChapter[];
}

